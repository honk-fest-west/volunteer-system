  <h1>Data Deletion Instructions</h1>
  <p>
    You can delete all data that the H!FW Volunteer system has for your account
    by:
  </p>
  <ol>
    <li>Signing in to your account.</li>
    <li>Click on your profile image in the upper left hand corner.</li>
    <li>Find the "Delete Account" button towards the bottom in red.</li>
    <li>Click the "Delete Account" button and confirm.</li>
  </ol>
  <p>
    Following the above steps will immediately delete all data that the H!FW
    Volunteer system has for your account. You will be signed out of your
    account and redirected to the home page. If you have any questions, please
    contact us at <a href="mailto: admin@honkfestwest.org"
      >admin@honkfestwest.org</a
    >.
  </p>
</article>
